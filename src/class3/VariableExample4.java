package class3;

public class VariableExample4{

     public static void main(String []args){
        int a = 1;
        Integer b = 2;
        // a(5) + b(10)를 더하면 3이 되어 c변수에 저장됩니다.
        int c = a + b;
        // 콘솔 출력
        System.out.println(" a + b = " + c);
        // 문자열 선언
        String d = "Hello";
        String e = "World";
        // d(Hello)와 f(World)의 문자열이 합쳐져서 HelloWorld 이 됩니다.
        String f = d + e;
        // 콘솔 출력
        System.out.println(" d + f = " + f);
     }
     
}
